package com.finalproject.soscanner.vo;

public class UserVO {
	
	private int u_uno;
	private String u_email;
	private String u_pass;
	private String u_passChk;
	private String u_nationality;
	private String u_language;
	private String u_token;
	
	
	
	public String getU_passChk() {
		return u_passChk;
	}
	public void setU_passChk(String u_passChk) {
		this.u_passChk = u_passChk;
	}
	public int getU_uno() {
		return u_uno;
	}
	public void setU_uno(int u_uno) {
		this.u_uno = u_uno;
	}
	public String getU_email() {
		return u_email;
	}
	public void setU_email(String u_email) {
		this.u_email = u_email;
	}
	public String getU_pass() {
		return u_pass;
	}
	public void setU_pass(String u_pass) {
		this.u_pass = u_pass;
	}
	public String getU_nationality() {
		return u_nationality;
	}
	public void setU_nationality(String u_nationality) {
		this.u_nationality = u_nationality;
	}
	public String getU_language() {
		return u_language;
	}
	public void setU_language(String u_language) {
		this.u_language = u_language;
	}
	public String getU_token() {
		return u_token;
	}
	public void setU_token(String u_token) {
		this.u_token = u_token;
	}
	
}
